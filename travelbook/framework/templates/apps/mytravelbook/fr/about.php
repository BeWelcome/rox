<h2>What is MyTravelBook?</h2>
<p>MyTravelBook is a tool for planning your next trip or keeping track with your friends at home.</p> 
<p>No matter if you go on a 10 month world trip or explore exciting cities at the weekend, gathering necessary information can be tedious. MyTravelBook helps you structure information. Create a trip, add stations and enter information. You may easily change the order of stations, add information later on, or share your trip with others! You can then print your trip, download it on your personal digital assistant or smartphone or access it form any inernet-connected computer. After a trip use it to remember and share memories and as an information source to other travelers.</p>
<p>MyTravelBook is and will be free to use, this is not a business. We are still in an early development phase, we apologize for bugs but are working hard to improve the system every day. We wish you a lot of fun using MyTravelBook and hope it is helpful. And do not hesitate posting your suggestons, bug reports, success stories using MyTravelBook. And one last point: we are constantly looking for help developing this project further, if you like to code or design webpages, why not join us?</p>

